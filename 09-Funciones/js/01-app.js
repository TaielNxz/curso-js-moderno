// Declaracion de funcion ( Function Declaration )
function sumar() {
    console.log(2 + 2);
}

// llamar a la funcion
sumar();


// Expresión de función ( Function Expression ) - Este tipo de funciones se asigna como si fuera una variable
const sumar2 = function() {
    console.log(3 + 3);
}

// se manda llamar de la misma forma
sumar2();